        /// </summary>
        /// <param name="gameTime">Provides a snapshot of timing values.</param>
        protected override void Update(GameTime gameTime)
        {
            // Allows the game to exit
            if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed)
                this.Exit();

            // TODO: Add your update logic here
            switch (currentState)
            {
                case Estates.Splashscreen:
                    InitializeState(splashScreen);
                    targetState = splashScreen.Update(gameTime);
                    DisposeState(splashScreen);
                    break;
                case Estates.Menu:
                    InitializeState(menu);
                    targetState = menu.Update(gameTime);
                    DisposeState(menu);
                    break;
                case Estates.Gameplay:
                    InitializeState(gameplay);
                    targetState = gameplay.Update(gameTime);
                    //foreach (CollisionTiles tile in map.CollisionTiles)
                    //    character.Collision(tile.Rectangle, map.Width, map.Height);

                    DisposeState(gameplay);
                    break;
                case Estates.Exit:
                    Exit();
                    break;
            }



            base.Update(gameTime);
        }

